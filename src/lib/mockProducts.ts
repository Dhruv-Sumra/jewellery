export function getMockProductsWithPricing(silverPrice: number = 245) {
  return mockProducts.map((product) => {
    const silverCost = product.weightInGrams * silverPrice;
    const subtotal = silverCost + product.makingCharges;
    const gst = subtotal * 0.03;
    const finalPrice = subtotal + gst;

    return {
      ...product,
      pricing: {
        silverCost: Math.round(silverCost * 100) / 100,
        makingCharges: product.makingCharges,
        subtotal: Math.round(subtotal * 100) / 100,
        gst: Math.round(gst * 100) / 100,
        finalPrice: Math.round(finalPrice * 100) / 100,
      },
      silverPricePerGram: silverPrice,
    };
  });
}
